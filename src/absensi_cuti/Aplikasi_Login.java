private void prosesLogin(){
    String kodeuser="";
    String password="";
    kodeuser=tkodeuser.getText();
    try{String sql="Select kd_user,nm_user,password, level"
            + " from user where kd_user='"+kodeuser+"' AND password='"+tpassword.getText()+"'";
  kon.rs=kon.st.executeQuery(sql);
  if(kon.rs.next()){
      if(tlevel.getText().equals("1")){
          Aplikasi_MenuUtama menu = new Aplikasi_MenuUtama();
          Aplikasi_Ganti_Password gantipass= new Aplikasi_Ganti_Password();
          menu.setLocationRelativeTo(null);
          menu.setVisible(true);
          menu.NamaUser=kon.rs.getString("nm_user");
          menu.KodeUser=kon.rs.getString("kd_user");
          menu.Level=kon.rs.getString("level");
          gantipass.Kode_User=kon.rs.getString("kd_user");
          this.dispose();
      }
      
  }
  else{
      JOptionPane.showMessageDialog(null, "KODE user atau PASSWORD SALAH");
      bersih();
      tkodeuser.setEnabled(true);
      tkodeuser.requestFocus();
  }
    }
    catch(SQLException e){
        JOptionPane.showMessageDialog(null, e);
    }
    
    }

